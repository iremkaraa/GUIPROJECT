    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String visitid = jTextField9.getText();
        editingVisitid = visitid;
        try{
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("select * from visits where visitid='" + visitid + "';");
            
            if(rs.next()==true){
                String country = rs.getString("countryName");
                String city = rs.getString("cityName");
                String year = rs.getString("year");
                String season = rs.getString("season");
                String feature = rs.getString("bestFeature");
                String comment = rs.getString("comment");
                String rating = rs.getString("rating");
                jTextField2.setText(country);
                jTextField3.setText(city);
                jTextField4.setText(year);
                jTextField5.setText(season);
                jTextField6.setText(feature);
                jTextField8.setText(rating);
                jTextField7.setText(comment);
            }
        } catch(SQLException ex){
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

