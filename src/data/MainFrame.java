    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean success = true;
        try{
            Statement stmt = conn.createStatement();
            stmt.executeUpdate("INSERT INTO visits VALUES(" + "'" + username + "'," + "'" + jTextField2.getText() + "'" + ","
                    + "'" + jTextField3.getText() + "'"
                    + "," + jTextField4.getText() + "," + "'" + jTextField5.getText() + "'" + ","
                    + "'" + jTextField6.getText() + "'" + "," + "'" + jTextField7.getText() +  "'," + jTextField8.getText() + ", null)");
            
            String cityName = jTextField3.getText();
            if(cityName.equals("tokyo") || cityName.equals("cologne") || cityName.equals("ankara") || cityName.equals("new york") || cityName.equals("pisa")){
                locationPhoto.showPhoto(cityName);
            }else{locationPhoto.setVisible(false);}
        }catch(SQLException ex){
            success = false;
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (success){
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText(""); 
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

